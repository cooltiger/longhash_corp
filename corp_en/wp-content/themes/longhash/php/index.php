          <dd>A 　いわゆる「音源審査」となります。エントリー時にご送付いただいた歌唱音源を基に、特に歌唱力に秀でていると判断される方々を上位より選出します。音源審査は、歌唱審査経験の豊富な音楽関係者が担当します。</dd>
