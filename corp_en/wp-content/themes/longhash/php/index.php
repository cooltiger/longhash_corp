        </dl>

        <dl>
          <dt>Q 　予選大会に出場しましたが、入賞できなかったので別の予選会場でもう一度チャレンジしたいです。可能ですか？</dt>
          <dd>A 　はい、複数の予選会場でのご出場は可能です。ご都合がつけば、4回全ての予選大会にご出場いただいても問題ございません。</dd>
        </dl>

