          <dt>Q 　選曲に迷っているので複数曲をエントリーしたいですが、可能ですか？</dt>
          <dd>A 　いいえ、同一予選会場へのエントリーは、お一人（お一組）につき一回（一曲）のみとなります。十分に吟味のうえ、歌唱曲をご決定ください。なお、仮に複数回エントリーをされた場合、エントリー日時の最も遅い応募のみが有効となり、それ以前のエントリーは無効となりますのでご了承ください（いわゆる「上書き方式」です）。</dd>
        </dl>

        <dl>
          <dt>Q 　既にエントリーを済ませましたが、曲を変えたいです。エントリー受付期間中であれば変更は可能ですか？</dt>
          <dd>A 　はい、希望曲にて再度エントリーのお手続きをしてください。エントリーはいわゆる「上書き方式」ですので、同じ方が同じ予選会場で再度エントリーをされた場合、過去のエントリーは無効となり、結果として最終的なご希望曲でのエントリーのみが有効となります。</dd>
        </dl>

        <dl>
          <dt>Q 　既にエントリーを済ませましたが、曲を変えたいです。エントリー受付期間を過ぎましたが変更は可能ですか？</dt>
          <dd>A 　いいえ、受付期間を過ぎてからのエントリー曲の変更はできません。予選大会へのご出場が決まった場合、歌唱曲はエントリー曲と同一曲となります。ただし、グランドチャンピオン大会に進出された際には、予選大会から歌唱曲を変更することは可能です。</dd>
        </dl>

        <dl>
          <dt>Q 　「ベスト東京ソング賞」の記載がありますが、こちらの大会は東京にまつわる曲でしかエントリーできないのですか？</dt>
          <dd>A 　いいえ、歌唱曲は自由です（LIVE DAM STADIUM配信曲）。お好きな曲、お得意な曲でのエントリーをお待ちしてします。</dd>
        </dl>

        <dl>
          <dt>Q 　沢田研二さんの「TOKIO」は東京ソングの対象になりますか？</dt>
          <dd>A 　「TOKIO」は「東京」の意であると解釈もできますが、恐縮ながら対象外とさせていただきます。意味で解釈すれば「江戸」など際限なく判断対象が増えるためです。「とうきょう」という読みで「東京」を意味する曲を東京ソングの対象といたします。</dd>
        </dl>

        <dl>
          <dt>Q 　「東京」を含む歌手の曲（例：東京事変）であれば全て東京ソングの対象になりますか？</dt>
          <dd>A 　歌手名は対象外とさせていただきます。歌手名ではなく楽曲において、歌詞やタイトルに「東京」のフレーズを持つ曲を東京ソングの対象といたします。東京事変の曲であれば、たとえば「群青日和」は1コーラス目に「東京」のフレーズがあるので該当します。</dd>
        </dl>

        <dl>
          <dt>Q 　エントリー音源は1コーラスのみの歌唱でも可能ですか？</dt>
          <dd>A 　はい、当大会は1コーラス歌唱の大会ですので、1コーラスのみ歌唱いただいた音源でもエントリーは可能です。</dd>
        </dl>

        <dl>
          <dt>Q 　1コーラスの切れ目のわかりにくい曲でエントリーする予定です。事前に切れ目を確認させていただくことは可能ですか？</dt>
          <dd>A 　はい、可能です。お問い合わせフォームにてご質問ください。ただし昨今は1コーラスの切れ目のわかりにくい曲も多々ございます。したがって「当大会における切れ目」は、事務局による判断にて決定させていただきます。「ここまで歌いたい！」といったご希望を承ることは致しかねますのでご了承ください。</dd>
        </dl>

