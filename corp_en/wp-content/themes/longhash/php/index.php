      <dl>
        <dt>Q 　「東京カラオケまつり2018」とはどのようなカラオケ大会ですか？</dt>
        <dd>A 　国際文化都市・東京を日本発祥の文化「カラオケ」で盛り上げることを目的とした大会です。広く出場者を募るために、都内複数の会場（計4会場）で予選大会を開催いたします。予選大会の入賞者は、8月開催のグランドチャンピオン大会への出場権を獲得されます。</dd>
      </dl>

      <dl>
        <dt>Q 　審査基準はどのようになりますか？「まつり」なのでパフォーマンス重視ですか？</dt>
        <dd>A 　予選大会・グランドチャンピオン大会ともに、歌唱力評価を基軸とした審査を行います。他、パフォーマンスに秀でたご出場者に対する特別賞も複数ございます。歌唱力自慢・パフォーマンス自慢いずれも歓迎いたします。</dd>
      </dl>

      <dl>
        <dt>Q 　「東京カラオケまつり2018」は東京都在住者しか出場できないのですか？</dt>
        <dd>A 　いいえ、東京都ご在住に限らず、どなたでもご出場いただけます。奮ってご応募ください。</dd>
      </dl>

      <dl>
        <dt>Q 　各予選大会へのエントリー方法を教えてください。</dt>
        <dd>A 　Webエントリーと郵送エントリーと、２種類ございます。いずれかの方法でエントリーをしてください。</dd>
      </dl>

